/**
 * 点类
 *
 * @author ZD
 * @date 2020-09-15 13:08
 */
public class Coordinates {
    /**
     * 长度
     */
    protected Double length;
    /**
     * 宽度
     */
    protected Double width;

    Coordinates(Double length, Double width) {
        this.length = length;
        this.width = width;
    }

}
